<?php
// สร้างคลาส (Class) ชื่อ Fruit เป็นแม่พิมพ์สำหรับสร้างออบเจกต์ผลไม้
class Fruit
{
    // คุณสมบัติ (Properties) หรือตัวแปรภายในคลาสสำหรับเก็บชื่อและสี
    public $name;
    public $color;

    // คอนสตรักเตอร์ (Constructor) ทำงานอัตโนมัติ "ทันทีที่มีการสร้างออบเจกต์ใหม่ (new)"
    function __construct($name, $color)
    {
        $this->name = $name;   // รับค่าชื่อที่ส่งมา ไปเก็บไว้ใน $name ของคลาส
        $this->color = $color; // รับค่าสีที่ส่งมา ไปเก็บไว้ใน $color ของคลาส
    }

    // ดีสตรักเตอร์ (Destructor) ทำงานอัตโนมัติ "เมื่อออบเจกต์ถูกทำลาย หรือสคริปต์ทำงานเสร็จสิ้น"
    function __destruct()
    {
        // แสดงข้อมูลผลไม้ตอนที่ออบเจกต์กำลังจะถูกทำลายหรือจบการทำงาน
        echo "Name: " . $this->name . ". Color: " . $this->color . ".<br>";
    }
}

// 1. สร้างออบเจกต์แอปเปิ้ล ( __construct ของ Apple ทำงานทันที)
$apple = new Fruit('Apple', 'Red');

// 2. สร้างออบเจกต์กล้วย ( __construct ของ Banana ทำงานทันที)
$banana = new Fruit('Banana', 'Yellow');

//เมื่อโค้ดทำงานมาถึงบรรทัดสุดท้ายนี้ (จบสคริปต์ PHP) 
// ออบเจกต์ทั้งหมดจะถูกทำลาย ทำให้ฟังก์ชัน __destruct ของแต่ละตัวทำงานอัตโนมัติและแสดงผลออกมาบนหน้าจอ
?>